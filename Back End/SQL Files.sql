/*
 *	CREATE DATABASE `HOSPITAL_MANAGEMENT_SYSTEM`
 */
DROP DATABASE IF EXISTS `HOSPITAL_MANAGEMENT_SYSTEM`;
CREATE DATABASE `HOSPITAL_MANAGEMENT_SYSTEM`;

USE `HOSPITAL_MANAGEMENT_SYSTEM`;


/*
 *	TABLE STRUCTURE FOR TABLE `APPOINTMENT`
 */
DROP TABLE IF EXISTS `APPOINTMENT`;

CREATE TABLE `APPOINTMENT` (
	`APP_ID` INT(10) NOT NULL AUTO_INCREMENT,
  	`P_ID` INT(10) NOT NULL,
	`BLOOD_PRESSURE` VARCHAR(32) NOT NULL,
	`BLOOD_SUGAR` VARCHAR(32) NOT NULL,
 	`WEIGHT` VARCHAR(32) NOT NULL,
  	`TEMPERATURE` VARCHAR(32) NOT NULL,
	`D_ID` INT(10) NOT NULL,
 	`APP_DATE` DATE NOT NULL,
	PRIMARY KEY (`APP_ID`)
) ENGINE=INNODB AUTO_INCREMENT=1 DEFAULT CHARSET=LATIN1;


/*
 *	TABLE STRUCTURE FOR TABLE `DOCTOR`
 */
DROP TABLE IF EXISTS `DOCTOR`;

CREATE TABLE `DOCTOR` (
  `D_ID` INT(10) NOT NULL AUTO_INCREMENT,
  `D_FNAME` VARCHAR(64) NOT NULL,
  `D_LNAME` VARCHAR(64) NOT NULL,
  `ADDRESS` VARCHAR(64) NOT NULL,
  `PHONE` VARCHAR(10) NOT NULL UNIQUE,
  `QUALIFICATION` VARCHAR(64) NOT NULL,
  `DEPARTMENT` VARCHAR(64) NOT NULL,
  `FEE` DECIMAL(10,2) NOT NULL,
  `DATE` DATE NOT NULL,
  `AGE` INT(10) NOT NULL,
  PRIMARY KEY (`D_ID`)
) ENGINE=INNODB AUTO_INCREMENT=1 DEFAULT CHARSET=LATIN1;


/*
 *	TABLE STRUCTURE FOR TABLE `NURSE`
 */
DROP TABLE IF EXISTS `NURSE`;

CREATE TABLE `NURSE` (
  `N_ID` INT(10) NOT NULL AUTO_INCREMENT,
  `N_FNAME` VARCHAR(64) NOT NULL,
  `N_LNAME` VARCHAR(64) NOT NULL,
  `ADDRESS` VARCHAR(64) NOT NULL,
  `PHONE` VARCHAR(10) NOT NULL UNIQUE,
  `QUALIFICATION` VARCHAR(64) NOT NULL,
  `DEPARTMENT` VARCHAR(64) NOT NULL,
  `DATE` DATE NOT NULL,
  `WARD` INT(10) NOT NULL,
  PRIMARY KEY (`N_ID`)
) ENGINE=INNODB AUTO_INCREMENT=1 DEFAULT CHARSET=LATIN1;
 
 
/*
 *	TABLE STRUCTURE FOR TABLE `PATIENT`
 */
DROP TABLE IF EXISTS `PATIENT`;

CREATE TABLE `PATIENT` (
  `P_ID` INT(10) NOT NULL AUTO_INCREMENT,
  `P_FNAME` VARCHAR(64) NOT NULL,
  `P_LNAME` VARCHAR(64) NOT NULL,
  `ADDRESS` VARCHAR(64) NOT NULL,
  `PHONE` VARCHAR(10) NOT NULL UNIQUE,
  `DATE` DATE NOT NULL,
  `DISEASE` VARCHAR(64) NOT NULL,
  `AGE` INT(2) NOT NULL,
  `SEX` VARCHAR(1) NOT NULL,
  PRIMARY KEY (`P_ID`)
) ENGINE=INNODB AUTO_INCREMENT=1 DEFAULT CHARSET=LATIN1;
 
 
/*
 *	TABLE STRUCTURE FOR TABLE `STAFF`
 */
DROP TABLE IF EXISTS `STAFF`;

CREATE TABLE `STAFF` (
  `S_ID` INT(10) NOT NULL AUTO_INCREMENT,
  `S_FNAME` VARCHAR(64) NOT NULL,
  `S_LNAME` VARCHAR(64) NOT NULL,
  `ADDRESS` VARCHAR(64) NOT NULL,
  `PHONE` VARCHAR(10) NOT NULL UNIQUE,
  `QUALIFICATION` VARCHAR(64) DEFAULT NULL,
  `DEPARTMENT` VARCHAR(64) NOT NULL,
  `DATE` DATE NOT NULL,
  PRIMARY KEY (`S_ID`)
) ENGINE=INNODB AUTO_INCREMENT=1 DEFAULT CHARSET=LATIN1;
 
 
/*
 *	TABLE STRUCTURE FOR TABLE `BILLINFO`
 */
DROP TABLE IF EXISTS BILLINFO;

CREATE TABLE `BILLINFO` (
	`BILL_ID` INT(10) NOT NULL AUTO_INCREMENT,
  	`P_ID` INT(10) NOT NULL,
	`D_ID` INT(10) NOT NULL,
	`BILL_DATE` DATE NOT NULL,
  	`TOTAL_AMOUNT` DECIMAL(10,2) NOT NULL,
	PRIMARY KEY (`BILL_ID`)
	/*
	FOREIGN KEY (`P_ID`) REFERENCES  `PATIENTS` (`ID`),
	FOREIGN KEY (`D_ID`) REFERENCES  `DOCTORS` (`ID`)
	*/
) ENGINE=INNODB AUTO_INCREMENT=1 DEFAULT CHARSET=LATIN1;

 
